package com.fp.exec;

public class ExecDTO {
	
	private int num ;
	private String teachar;
	private String title;
	private String contents;
	private String result;
	private String classKind;
	private String quizLevel;
	private String basicClass;
	private String basiccode;
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTeachar() {
		return teachar;
	}
	public void setTeachar(String teachar) {
		this.teachar = teachar;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getClassKind() {
		return classKind;
	}
	public void setClassKind(String classKind) {
		this.classKind = classKind;
	}
	public String getQuizLevel() {
		return quizLevel;
	}
	public void setQuizLevel(String quizLevel) {
		this.quizLevel = quizLevel;
	}
	public String getBasicClass() {
		return basicClass;
	}
	public void setBasicClass(String basicClass) {
		this.basicClass = basicClass;
	}
	public String getBasiccode() {
		return basiccode;
	}
	public void setBasiccode(String basiccode) {
		this.basiccode = basiccode;
	}
	
	

}
